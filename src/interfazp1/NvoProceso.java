    private void numeroPKeyPressed(java.awt.event.KeyEvent evt)//GEN-FIRST:event_numeroPKeyPressed
    {//GEN-HEADEREND:event_numeroPKeyPressed
        // TODO add your handling code here:
        if ('\n' == evt.getKeyChar() && numeroP.getSelectedIndex() != 0)
        {
            CtrlInterfaz.cambia(okB);
        }else
        {
            if ('\n' == evt.getKeyChar())
            {
                Mensajes.error(this, "Seleccione un nivel de Prioridad");
            }
        }
    }//GEN-LAST:event_numeroPKeyPressed

    private void okBKeyPressed(java.awt.event.KeyEvent evt)//GEN-FIRST:event_okBKeyPressed
    {//GEN-HEADEREND:event_okBKeyPressed
        // TODO add your handling code here:
        if ('\n' == evt.getKeyChar())
        {
            okBActionPerformed(null);
        }
        if (evt.getKeyCode()==KeyEvent.VK_RIGHT || evt.getKeyCode()== KeyEvent.VK_LEFT)
        {
            CtrlInterfaz.cambia(cancelarB);
        }
    }//GEN-LAST:event_okBKeyPressed

