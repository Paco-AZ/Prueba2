    private void okBActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_okBActionPerformed
    {//GEN-HEADEREND:event_okBActionPerformed
        // TODO add your handling code here:
        if (!nombreP.getText().equals("") && !numeroQ.getText().equals("") && numeroP.getSelectedIndex() != 0)
        {
            if (Principal.x == null)
            {
                Principal.x = new Ejecucion();
            }
            Principal.x.subirProc(new Proceso(numeroP.getSelectedIndex(), nombreP.getText(), Integer.parseInt(numeroQ.getText())),this);
            dispose();
        }else
        {
            Mensajes.error(this, "Los campos estan incompletos");
        }
    }//GEN-LAST:event_okBActionPerformed

